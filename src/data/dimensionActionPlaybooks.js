function buildFallbackPlaybook(label) {
  return {
    diagnosis: {
      low: `你在【${label}】上的问题不是完全不会，而是还没有形成稳定的方法和可复用动作，所以一到真实任务里发挥就容易起伏。`,
      mid: `你在【${label}】上已经有一定基础，但还没把经验沉淀成固定步骤，复杂任务里稳定性仍然不够。`,
      high: `你在【${label}】上已经有不错基础，接下来更重要的是把这项能力沉淀成可复用的方法和案例证据。`,
    },
    advice: {
      low: `先把【${label}】拆成最基本的步骤练稳，再补复杂场景应用，别继续停留在零散尝试。`,
      mid: `先把【${label}】的固定方法做稳定，再练在真实任务里的应用和复盘。`,
      high: `先把你在【${label}】上的有效做法沉淀下来，再补更高要求场景下的结果表达。`,
    },
    actionPlan: {
      howToDo: [
        `每周围绕【${label}】做 2 次小练习，并记录结果和问题。`,
        `把练习输出沉淀成可复盘的文档、案例或模板。`,
      ],
      howToPlan: [
        "第 1 阶段：先补基础概念和固定步骤。",
        "第 2 阶段：再做真实任务训练并沉淀成果。",
      ],
      whatToLearn: [`【${label}】基础原理`, `【${label}】常见方法`, `【${label}】典型训练题或小项目`],
    },
  };
}

export function getDimensionDevelopmentCard({ dimension, label, score }) {
  const playbook = PLAYBOOKS[dimension] ?? buildFallbackPlaybook(label);
  const band = getScoreBand(score);

  return {
    currentDiagnosis: playbook.diagnosis[band],
    improvementAdvice: playbook.advice[band],
    actionPlan: {
      howToDo: [...playbook.actionPlan.howToDo],
      howToPlan: [...playbook.actionPlan.howToPlan],
      whatToLearn: [...playbook.actionPlan.whatToLearn],
    },
  };
}
